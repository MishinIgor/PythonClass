# –ï—Å–ª–∏ –¥–µ—Å—è—Ç–∫–∏, —Ç–æ –≤—ã–≤–µ—Å—Ç–∏ –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –≤—Å–µ—Ö —Ü–∏—Ñ—Ä
# –ï—Å–ª–∏ –µ–¥–∏–Ω–∏—Ü—ã, —Ç–æ –≤—ã–≤–µ—Å—Ç–∏ —á–∏—Å–ª–æ –Ω–∞ –æ–±–æ—Ä—Ç

chislo = int(input('–í–≤–µ–¥–∏—Ç–µ —Ç—Ä—ë—Ö–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ: '))

if not(99<chislo<1000):
    print('–£ –≤–∞—Å –≤—ã–ø–∞–ª–æ üß† –∏ –≤—ã –Ω–∞–ø—É—Ç–∞–ª–∏')
else:
    sot = chislo // 100 # // –≤—ã–¥–µ–ª—è–µ–º —Ü–µ–ª—É—é —á–∞—Å—Ç—å –æ—Ç –¥–µ–ª–µ–Ω–∏—è. 537 // 100 = 5
    des = chislo//10%10 # 537 // 10 = 53, 53 % 10 = 3. % - –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏—è
    ed = chislo % 10 # 537 % 10 = 7
    mx = max(sot, des, ed)

    if mx == sot:
        summa = sot+des+ed
        print(f'–°—É–º–º–∞ –≤—Å–µ—Ö —Ü–∏—Ñ—Ä: {summa}')
        if summa>des*ed:
            print('–û–≥–æ! –ö–∞–∫–æ–π —Å—Ç–∞—Ä—ã–π —Ö–ª–µ–±')
    elif mx == des:
        print(f'–ü—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –≤—Å–µ—Ö —Ü–∏—Ñ—Ä: {sot*des*ed}')
    else:
        print(f'{ed}{des}{sot}')