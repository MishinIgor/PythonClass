            if zn1 == zn2 == zn3:
                print(f'–í–∞—à –±–∞–ª–∞–Ω—Å: {balans-300+2500}')
            elif zn1 == zn2 or zn1 == zn3 or zn2==zn3:
                print(f'–í–∞—à –±–∞–ª–∞–Ω—Å: {balans-300+250}')
